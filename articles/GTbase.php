    <h1>GT Base 與 Lock 鬆緊度</h1>
    <aside>2019-07-31</aside><br><br>
    <article>
        <p>GT 系統雖說 Chip 決定 Lock 齒的鬆緊度，可是其實 Base 的影響大更多。<br>以下作為例子的 Base，黃色是【A】、藍色是【B】、綠色是【F】。</p>
        <h2>➊ Base 中央部分厚度</h2>
        <p>為何同一個 Chip 裝上不同 Base，Lock 齒位的凸出程度不同？</p>
        <figure>
            <figcaption>【F】Base 上的 Chip 齒位較低</figcaption>
            <img src='GT01.jpg'>
        </figure>
        <p>下圖可以看到，某些 Base 中間的 Driver 承托位看似較長，這樣 Chip 的 Lock 齒位便會較「埋入」。</p>
        <figure>
            <figcaption>【F】Base 的 Driver 承托位較凸出</figcaption>
            <img src='GT02.jpg'>
        </figure>
        <p>從 Base 上面看就能看到原因：部分 Base 的中央部份較厚，就會做成 Driver 承托位看似較長。</p>
        <figure>
            <figcaption>【F】Base 的中央部份比【A】Base 較厚</figcaption>
            <img src='GT03.jpg'>
        </figure>
        <p>結果就是，Base 的中央部份愈厚，Driver 被安裝的位置就愈低，與 Chip 的距離就愈大。</p>
        <figure>
            <figcaption>【F】Base 的 Driver 位置比【A】Base 低</figcaption>
            <img src='GT04.jpg'>
        </figure>
        <p>這樣，Driver 上負責 Lock 的白色凸起便不能完全嵌入 Chip 齒的凹位，齒位再深也是枉然！</p>
        <figure>
            <figcaption>Driver 與 Chip 距離愈大，<em>變相 Chip 齒位愈淺</em>（示意圖）</figcaption>
            <img src='GT05.jpg'>
        </figure>
        <p><i>動腦筋時候</i>：此因素也對【幻】Weight 的效果做成直接影響！</p>
        <h2>➋ Driver 承托位的直柱</h2>
        <p>留意某些 Base 上的 Driver 承托位旁，可發現有一凸出直柱；某此 Base 則沒有。</p>
        <figure>
            <figcaption>【B】Base 沒有直柱</figcaption>
            <img src='GT06.jpg'>
        </figure>
        <p>把 Driver 完全扭上 Base，再退格，明顯感覺得到此直柱帶來的阻力。</p>
        <figure>
            <figcaption>直柱做成了一點阻礙</figcaption>
            <img src='GT07.jpg'>
        </figure>
        <p>故此，<em>此直柱的突出程度，參與了第一格 Lock 鬆緊度的決定。</em>如果沒有直柱，Base 中央又較厚，就會感覺很鬆。</p>
        <h2>➌ Driver 承托位的大小</h2>
        <p>有留意到不同的 Base 承托位的闊度似乎有不同？</p>
        <figure>
            <figcaption>似乎【F】Base 承托位比【A】Base 闊</figcaption>
            <img src='GT08.jpg'>
        </figure>
        <figure>
            <figcaption>這樣看就更明顯了！</figcaption>
            <img src='GT09.jpg'>
        </figure>
        <p>把 Driver 完全扭上 Base，再把 Driver 上下搖動，明顯感受到可活動空間有不同。</p>
        <figure>
            <figcaption>【A】Base 上的 Driver 有空間被上下搖動；【F】則近乎沒空間。</figcaption>
            <img src='GT10.jpg'>
        </figure>
        <p>承托位較闊，就能把 Driver 夾緊，Lock 亦較緊。但由於是都是塑膠關係，未必真的有明顯的改善。<br>可是同時會帶來一項我認為更重要的好處：把 Driver 夾緊後，<em>Driver 便能完全置中，提升旋轉穩定度及持久。</em></p>
        <h2>➍ Driver 承托位的花紋</h2>
        <p>再留意一下承托位？</p>
        <figure>
            <figcaption>【B】Base 呈橫「ʃ」字的圖案，【A】Base 呈階梯式的「凹」字形。</figcaption>
            <img src='GT11.jpg'>
        </figure>
        <p>就如輪胎或鞋底的坑紋，此類圖案會影響摩擦力。但由於承托位面積不大，故此項因素影響較微小。</p>
        <h2>➎ Chip 扣位的大小</h2>
        <p>留意 Base 上兩個卡著 Chip 的凸起：</p>
        <figure>
            <figcaption>【A】Base 的卡扣被【F】Base 闊。</figcaption>
            <img src='GT12.jpg'>
        </figure>
        <p>由於 GT 代的 Lock 分離式設計，陀螺被攻擊至退 Lock 時，Chip 會有空間被向上推，結果 Lock 齒位實際有效部分更淺，故 GT 陀螺比超 Z 代等更鬆。</p>
        <p>Base 上卡位的闊窄可能會影響 Chip 能否好好固定於 Base 上，繼而影響鬆緊度。但較難有理據地證明是否真的愈闊就愈能把 Chip 固定好。</p>
        <br>
        <p>結論：<strong>陀螺鬆緊度，Base 才是主宰！</strong>因為除了上述的設計影響，本身其形狀是更 critical 的因素。但既然 Base 是主要接觸部，在其加上 Lock 鬆緊控制設計也是需要；否則人人也會用最圓的 Base 加上最緊的 Chip，又會破壞平衡了。<br>

    <p>只要在日本推出的日子左右的時候（4月尾）有留意日本人的討論，就知道可憑目測外盒而決定是否籤王（翼龍）。</p> 
    <p>方法是留意盒背的編號刻印（C1017）：</p> 
    <p>「0」字是ellipse形（全曲線）而不是oval形（兩邊是直線）的話，就只有翼龍、黑魔狼及橙幻影此3個可能；<br> 
    當中，如果「7」字比其他數字大及有一段距離，就是籤王！</p> 
    <p>不過，此方法似乎只適用於第一批貨品，其後的已失效⋯⋯</p> 
